public class Supir {
    public String nama_supir;
    private String alamat;
    private int nohp_supir;

    public String getNama_supir() {
        return nama_supir;
    }

    public void setNama_supir(String nama_supir) {
        this.nama_supir = nama_supir;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public int getNohp_supir() {
        return nohp_supir;
    }

    public void setNohp_supir(int nohp_supir) {
        this.nohp_supir = nohp_supir;
    }
}
